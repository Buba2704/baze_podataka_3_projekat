    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            
            if(txtNaziv.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Morate uneti naziv magacina!");
                return;
            }
            
            m.setNazivMagacina(txtNaziv.getText());
            m.setRadnik((Radnik)cmbRadnik.getSelectedItem());
            m.setNamena(String.valueOf(cmbNamena.getSelectedItem()));
            
            DateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
            String datumNovi = null;
            String datumStari = null;
            if(txtDatum.getDate()!=null){
                datumNovi = dateFormat.format(txtDatum.getDate());
            }
            if(datumPre!= null){
                datumStari = dateFormat.format(datumPre);
            }
            
            if (datumStari == null && datumNovi == null) {
                m.setPok(0);
            }else if(datumStari == null && datumNovi!=null || datumStari != null && datumNovi==null || !datumStari.equals(datumNovi)){
                m.setPok(1);
                m.setFormatDate(datumNovi);
            } else{
                m.setPok(0);
            }
            
            ClientController.getInstance().updateMagacin(m);
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen magacin.");
            lm.refreshTable();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            lm.refreshTable();
            this.dispose();
        }
        
                
    }//GEN-LAST:event_btnIzmeniActionPerformed

