    private void popuniRadnike() {
        try {
            ArrayList<Radnik> radnici;
            
            radnici = ClientController.getInstance().getAllRadnik();
            cmbRadnik.removeAllItems();
            
            for(Radnik r: radnici){
                cmbRadnik.addItem(r);
            }
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FormIzmenaMagacina.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniNamenu() {
        try {
            ArrayList<Namena> namene;
            
            namene = ClientController.getInstance().getAllNamena();
            cmbNamena.removeAllItems();
            
            for(Namena n: namene){
                cmbNamena.addItem(n);
            }
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FormIzmenaMagacina.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
   

