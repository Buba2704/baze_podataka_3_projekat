    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            
            if(txtSifra.getText().isEmpty() || txtNaziv.getText().isEmpty() || txtMaticniBroj.getText().isEmpty() ||  txtPIB.getText().isEmpty() || txtTekuciRacun.getText().isEmpty() || txtAdresa.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Morate popuniti sva polja!");
                return;
            }
            p.setNazivPartnera(txtNaziv.getText());
            p.setMaticniBroj(Long.parseLong(txtMaticniBroj.getText()));
            p.setPIB(Long.parseLong(txtPIB.getText()));
            p.setTekuciRacun(Long.parseLong(txtTekuciRacun.getText()));
            p.setAdresa(txtAdresa.getText());
            p.setMesto((Mesto)cmbMesto.getSelectedItem());
            
            ClientController.getInstance().updatePoslovniPartner(p);
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen poslovni partner.");
            lp.refreshTable();
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(FormIzmenaPoslovnogPartnera.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed



