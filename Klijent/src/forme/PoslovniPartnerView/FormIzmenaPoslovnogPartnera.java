    private void popuniMesta() {
        ArrayList<Mesto> mesta;
        
        try {
            mesta = ClientController.getInstance().getAllMesta();
      
        cmbMesto.removeAllItems();
        for(Mesto m: mesta){
            cmbMesto.addItem(m);
        }
        } catch (Exception ex) {
            Logger.getLogger(FormIzmenaPoslovnogPartnera.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
