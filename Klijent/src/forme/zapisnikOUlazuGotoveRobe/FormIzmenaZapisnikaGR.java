    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {
            if(txtEvidencioni.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Morate uneti evidencioni broj!");
                return;
            }
            if(txtDatum.getDate() == null){
                JOptionPane.showMessageDialog(this, "Morate uneti datum dokumenta!");
                return;
            }
            
            z.setEvidencioniBroj(Long.parseLong(txtEvidencioni.getText()));
            z.setOznaka(txtOznaka.getText());
            DateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
            String datum = dateFormat.format(txtDatum.getDate());
            z.setDatumDokumenta(txtDatum.getDate());
            z.setFormatDate(datum);
            z.setOrgJed((OrganizacionaJedinica)cmbOrgJed.getSelectedItem());
            z.setRadnik((Radnik)cmbRadnik.getSelectedItem());
            z.setMagacin((Magacin)cmbMagacin.getSelectedItem());
            
            ClientController.getInstance().updateZapisnikGR(z);
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen zapisnik.");
            FormListaZapisnikaGR lz = (FormListaZapisnikaGR) getParent();
            lz.refreshTable();        
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(FormIzmenaZapisnikaGR.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

