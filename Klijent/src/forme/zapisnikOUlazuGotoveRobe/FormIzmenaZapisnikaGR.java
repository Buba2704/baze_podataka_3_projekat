    private void popuniMagacine() {
        try {
            ArrayList<Magacin> magacini;
            
            magacini = ClientController.getInstance().getAllMagacin();
            cmbMagacin.removeAllItems();
            
            for(Magacin m: magacini){
                cmbMagacin.addItem(m);
            }
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FormIzmenaZapisnikaGR.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }

    private void popuniRadnike() {
        try {
            ArrayList<Radnik> radnici;
            
            radnici = ClientController.getInstance().getAllRadnik();
            cmbRadnik.removeAllItems();
            
            for(Radnik r: radnici){
                cmbRadnik.addItem(r);
            }
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FormUnosZapisnikaGR.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniOrgJed() {
        try {
            ArrayList<OrganizacionaJedinica> jedinice;
            
            jedinice = ClientController.getInstance().getAllOrgJed();
            cmbOrgJed.removeAllItems();
            
            for(OrganizacionaJedinica j: jedinice){
                cmbOrgJed.addItem(j);
            }
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FormUnosZapisnikaGR.class.getName()).log(Level.SEVERE, null, ex);
        }
    }



